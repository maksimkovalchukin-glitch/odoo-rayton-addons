print('=== Імпорт шаблону СЕС ===')

# ── 1. Знайти або створити шаблон-проект 'СЕС' ───────────────────────────────
template = env['project.project'].search(
    [('name', '=', 'СЕС'), ('active', 'in', [True, False])], limit=1
)

if template:
    print(f'Шаблон СЕС вже існує (id={template.id}) — оновлюємо')
    # Очищаємо існуючі завдання шаблону
    old_tasks = env['project.task'].search([('project_id', '=', template.id)])
    print(f'  Видаляємо {len(old_tasks)} старих завдань...')
    old_tasks.unlink()
    # Від'єднуємо старі стадії від цього проекту
    old_stages = template.type_ids
    print(f'  Від\'єднуємо {len(old_stages)} старих стадій...')
    template.write({'type_ids': [(5, 0, 0)]})  # clear M2M
else:
    print('Шаблон СЕС не знайдено — створюємо...')
    template = env['project.project'].create({
        'name': 'СЕС',
        'active': True,
        'privacy_visibility': 'followers',
    })
    print(f'  Створено шаблон id={template.id}')

env.cr.commit()

# ── 2. Створити стадії і завдання ─────────────────────────────────────────────
print(f'\nСтворюємо {len(STAGES_TASKS)} стадій...')
total_tasks = 0

for seq, (stage_name, task_names) in enumerate(STAGES_TASKS, start=10):
    # Знайти або створити task.type (стадію)
    stage = env['project.task.type'].search([('name', '=', stage_name)], limit=1)
    if not stage:
        stage = env['project.task.type'].create({
            'name': stage_name,
            'sequence': seq * 10,
        })
        print(f'  + Стадія: {stage_name}')
    else:
        print(f'  ~ Стадія існує: {stage_name} (id={stage.id})')

    # Прив'язати стадію до шаблону (M2M)
    template.write({'type_ids': [(4, stage.id)]})

    # Створити завдання
    for task_seq, task_name in enumerate(task_names, start=1):
        env['project.task'].create({
            'name': task_name,
            'project_id': template.id,
            'stage_id': stage.id,
            'sequence': task_seq * 10,
            'description': False,
        })
        total_tasks += 1

    env.cr.commit()
    print(f'    → {len(task_names)} завдань додано')

# ── Підсумок ──────────────────────────────────────────────────────────────────
print(f'\n=== Готово ===')
print(f'Шаблон: СЕС (id={template.id})')
print(f'Стадій: {len(STAGES_TASKS)}')
print(f'Завдань: {total_tasks}')

# Перевірка
task_count = env['project.task'].search_count([('project_id', '=', template.id)])
